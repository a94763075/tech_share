class Person:
  def __init__(self,first,last):
    self.firstname = first
    self.lastname = last

class Employee:
  def __init__(self,first,last, employee_id,salary):
    self.firstname = first
    self.lastname = last
    self.employee_id = employee_id
    self.salary = salary

class Intern:
  def __init__(self, first, last, intern_id, grades):
    self.firstname = first
    self.lastname = last
    self.intern_id = intern_id
    self.grades = grades


    